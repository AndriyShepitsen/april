<h2>Short Sale vs Foreclosure</h2> 
    <hr/>

<p>As Investors and Buyers, coming to the aid of a distressed Seller is what we’re supposed to be highly skilled at. Depending on their situation, and the condition of the home, their options may become more and more limited. It may come down to not what they want, but what options they have left.</p>

<p>In the case of choosing a short sale over a foreclosure, typically, the answer seems to be obvious from the beginning. If there is more debt owed on the property than what it’s worth, or what the Buyer is willing to pay, clearly the debt needs to be negotiated down if they plan to sell.</p>
<p>But, some Sellers may just want to capitulate and walk away. If it seems like a viable lead though, you may want to educate the homeowners on the pros and cons of the decision they’re considering.</p>

<p>If they’re not motivated and uncooperative, save your breath. No amount of sensibility about the deal structure will engage an uninterested party, so make sure you’re qualifying the Seller just as hard as you’re qualifying the deal.</p>

<p>If it’s a matter of needing to be educated to make a decision though, let’s explore when a short sale may be better for them over when it might be better for them to walk away. </p>

<h4 class="subheader">Short Sale Pro’s for Homeowners</h4>
<hr/>

<ol class="shortSaleVSForeclosure">
  <li>Short Sales have gotten easier – may be able to complete one in a few months</li>
  <li>May not have to miss any payments</li>
  <li>May automatically qualify for a short sale (in the case of a divorce, disease, and disability)</li>
  <li>Will not have to fix up or do anything to the house</li>
  <li>May be better on their credit</li>
  <li>May be able to get moving money from the bank $3,000-$30,000</li>
  <li>May not have to pay taxes on forgiven debt</li>
  <li>May not owe anything after sale for difference of what was owed vs sales price</li>
  <li>Free rent while living in home during short sale process</li>
</ol>

<h4 class="subheader">Short Sale Con’s for Homeowners</h4>
<hr/>
<ol class="shortSaleVSForeclosure">
  <li>Can be frustrating if not working with seasoned professional</li>
  <li>Bank may be able to come after Seller later for money if Lenders don’t waive deficiency rights</li>
  <li>May lose home to foreclosure if Buyer isn’t serious and backs out (this has a lot to do with #1)</li>
  <li>Bank may be uncooperative and turn down reasonable offers/issue approvals at inflated pricing</li>
</ol>
<p>In some of these cases, it may be better for the homeowner to exercise every option until the home goes to foreclosure. For instance, here in Arizona, we have strong anti-deficiency statues that protect homeowners that lose a home through Trustee Sale. In these cases, the lien holders lose their deficiency rights through the Trustees Sale, however; the anti-deficiency statutes only come into play with the foreclosure, NOT a short sale.</p>

<p>If the debt holders on the short sale are unwilling to waive those rights on their approval letters, and the homeowner would otherwise legally be divorced of any further obligation if they let it go to foreclosure, the short sale may not be the best option. (It can be a little tricky with debt like Home Equity lines, cash out refinances, so either way the Seller will need to talk to an attorney).</p>

<h4 class="subheader">Buyers Perspective – they NEED you</h4>
<hr/>
<p>Now where does your value add come in as a cash buyer/investor? To some effect, it’s one of the most important parts of the transaction!</p>

<p>You can let the Listing Agent and/or homeowner know (in fact, you may even want to create a Cover Letter for this to turn in with your Offers!)</p>
<ol class="shortSaleVSForeclosure">
  <li>You are a serious cash buyer that is aware of the short sale process</li>
  <li>You wish to help make the process as easy on everyone as possible. You like being educated and would prefer to be communicated with on a weekly basis.</li>
  <li>If the Listing Agent can provide you with the following information, it would greatly help get the transaction off on the right foot:</li>
	<ul class="square">
		<li>How many short sales have they done?</li>
		<li>Who are the Lenders on this property?</li>
		<li>Is there an HOA? If so, are they behind?</li>
		<li>Is the Seller cooperative?</li>
		<li>Where are they at with the process with the bank?</li>
		<li>Have they pulled title?</li>
		<li>Has the bank done a valuation? If so, when? For how much?</li>
	</ul>
</ol>

<p>All of this opens the dialogue for you the Buyer knowing what you’re looking to work with, and potentially save yourself months of frustration. Gather as much information from the get go and “prepare or pass” on the short sale, according to your findings.</p>


