<h2>What is a Short Sale</h2> 
    <hr/>
    <p>A short sale is a sale of real estate in which the proceeds from selling the property will fall short of the balance of debts secured by liens against the property, and the property owner cannot afford to repay the liens' full amounts, and whereby the lien holders agree to release their lien on the real estate and accept less than the amount owed on the debt. Any unpaid balance owed to the creditors is known as a deficiency. Short sale agreements do not necessarily release borrowers from their obligations to repay any deficiencies of the loans, unless specifically agreed to between the parties. However, in Illinois, legislation was passed to preclude deficiencies after a short sale is approved. The same is true of lenders on first loans and lenders on second loans - once the short sale is approved, no deficiencies are permitted after the short sale.</p>
    <p>A short sale is often used as an alternative to foreclosure because it mitigates additional fees and costs to both the creditor and borrower. Both often result in a negative credit report against the property owner.</p>
